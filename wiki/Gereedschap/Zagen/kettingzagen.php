<h3>Kettingzaag</h3>
        <p>Een kettingzaag (ook motorzaag, motorkettingzaag of boomzaag genoemd) is een draagbare machine om mee te zagen. De belangrijkste functie is hout zagen.</p>
        <br>

    <h3>Verschillende typen</h3>
        <p>Er zijn verschillende typen aandrijving voor kettingzagen: pneumatisch, hydraulisch, elektrisch en door middel van een verbrandingsmotor (vaak ook motorkettingzaag genoemd). De elektrische kettingzaag en de motorkettingzaag zijn het meest gangbaar.</p>
        <br>

    <h3>Elektrische kettingzaag</h3>
        <p>Elektrische kettingzagen zijn met name bedoeld voor particulier gebruik. Ze zijn milieuvriendelijker en maken doorgaans minder lawaai dan kettingzagen op benzine. Ze zijn praktischer in gebruik doordat ze lichter zijn. Elektrische kettingzagen zijn vaak goedkoper in aanschaf. Nadeel is het snoer bij sommige elektrische kettingzagen waardoor de actieradius beperkt is, maar draadloze elektrische kettingzagen bestaan ook. Deze werken op een accu.</p>
        <br>

    <h3>kettingzaag met brandstofmotor</h3>
        <p>Benzinekettingzagen hebben bijna zonder uitzondering een tweetaktmotor. Benzine-aangedreven kettingzagen hebben het voordeel dat ze een grotere mobiliteit hebben en krachtiger zijn dan elektrische kettingzagen.
        Er zijn verschillende typen benzine-kettingzaag verkrijgbaar:</p>
        <ul>
            <li>Gewone motorkettingzaag = variërend van 30 cc tot 121 cc.</li>
            <li>Tophandle = licht type kettingzaag. Hoewel deze machines bediend kunnen worden met één hand, is dit verboden. Wordt vooral gebruikt in de fruitteelt en de boomverzorging.</li>
            <li>motorstokzaag = kettingzaag op een stok om takken op hoogte door te zagen.</li>
        </ul>
