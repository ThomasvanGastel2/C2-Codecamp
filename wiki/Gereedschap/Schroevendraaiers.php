    <h3>Schroevendraaiers</h3>
        <p>Een schroevendraaier is een stuk gereedschap waarmee schroeven worden aangebracht en verwijderd. 
        Het bestaat uit een handvat met een metalen steel waarvan het uiteinde zo gevormd is dat het in de kop van een schroef past.
        Doorgaans worden schroeven rechtsom vastgedraaid en linksom los. 
        </p>        
        <br>
    
        <p>
        Schroevendraaiers zijn in velerlei soorten, afmetingen en kwaliteiten verkrijgbaar. 
        Er bestaan schroevendraaiers met een platte punt, alsook met een ster- of kruiskop. 
        Goede schroevendraaiers zijn vervaardigd van speciale staalsoorten. 
        Bij een juiste toepassing zijn ze praktisch onbreekbaar. 
        Het heft van een schroevendraaier wordt meestal gemaakt van een slagvaste, onbreekbare kunststof. 
        Soms vervaardigt men het heft nog van een taaie houtsoort. 
        De grootte van schroevendraaier en schroef dienen bij elkaar te 'passen'.
        </p>
        <br>
  
        <p>In plaats van schroevendraaiers worden ook wel losse schroefbits in een schroefboormachine gebruikt. 
        Deze zeskantige stiftjes zijn er in velerlei vormen en lengten. 
        Zo zijn er kruis- of ster-, platte voor sleuf, zeskantige maar ook vierkante vormen. 
        Variërend in lengte van enkele centimeters tot meer dan 10 centimeter De bits worden in een – meestal magnetische – houder geplaatst die vervolgens in de schroef- dan wel boormachine wordt geklemd. 
        Toch gebruikt men niet voor alle schroeven een schroefboormachine. Horlogemakers en instrumentmakers gebruiken voor het fijne werk speciale kleine schroevendraaiers.</p>
        <br>
    
        <p>Veelgebruikte types schroevendraaiers:
        <br>
        <br>
            sleufkopschroevendraaier (plat);<br>
            phillipsschroevendraaier PH (kruiskop zonder verdere markering);<br>
            pozidrivschroevendraaier PZ (kruiskop met extra sterretje).<br>
            <br>
        </p>      
        
    
        <p>De laatste twee types lijken op elkaar en worden daardoor weleens verwisseld.
        De Pozidriv heeft vier extra ribben ten opzichte van de Philips-uitvoering.
        Alleen een goede passing is er indien de juiste combinatie schroevendraaier/schroef wordt toegepast. 
        Bij gebruik van het juiste Pozidriv-schroefbit of schroevendraaier zal de schroef ook bij horizontaal houden van de schroef in de schroevendraaier of bit blijven zitten.</p>
