    <h3>Frisdrank</h3>
    <p>
        Frisdrank is het in 1956 door reclamemaker Dick Schiferli bedachte woord voor koolzuurhoudende limonade. Het wordt ook wel priklimonade of prik genoemd. In ruimere zin worden, volgens de Nederlandse Warenwet, ook niet-koolzuurhoudende limonades, sportdranken en energiedranken tot de frisdranken gerekend.
        Een typische frisdrank bestaat uit koolzuurhoudend water, waaraan suiker, aroma's (of echte vruchtenextracten) en eventueel andere hulpstoffen zijn toegevoegd.
        Frisdrank behoort tot de rode zone van de voedingsdriehoek (buiten de voedingsdriehoek).
        In het Surinaams-Nederlands wordt deze drank 'soft' genoemd, afgeleid van het Engelse softdrink.
